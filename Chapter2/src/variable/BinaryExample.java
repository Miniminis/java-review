package variable;

public class BinaryExample {

    public static void main(String[] args) {

        int a = 10;
        int b = 0B1010;
        int c = 012;
        int d = 0XA;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);

        /*
        * 10
        10
        10
        10
        * */

    }
}
